                     <br /><br />
                     <strong class="highlited-text">The lohn system consists of an economic business, carried out on a contractual basis,</strong> between two companies of different countries. One company performs, for a retribution in cash or in kind, a custom-made product according to the design, drawings, usually with raw materials and materials of the other company, which keep the right to trade that product under its own trademark.<br /><br />
                The low production cost in emerging economies makes these markets very attractive for the production of consumer goods.<br />
                <strong class="highlited-text">The main benefit of producing goods in these markets is the exceedingly low production prices</strong> which maximize profits.<br />
